<template>
  <figure :class="[getThemeClass('figure'), hasAspectRatio]">
    <div :class="getThemeClass('aspectRatio')">
      <picture v-if="src" :class="getThemeClass('picture')">
        <source v-if="src.webp" :data-srcset="src.webp" type="image/webp" />
        <source v-if="srcset" :data-srcset="srcset" />

        <img
          v-bind="$attrs"
          :alt="srcAlt"
          :src="srcImg"
          :data-src="srcImg"
          :class="[
            getThemeClass('image'),
            !disableLazyLoad && 'lazyload blur-up'
          ]"
        />
      </picture>

      <template v-else>
        <img v-if="usePlaceholder" :src="placeholderImage" />
      </template>
    </div>
    <slot v-bind="{ caption, copyright }" name="content">
      <span>{{ caption }}</span>
      <span v-if="copyright">&copy; {{ copyright }}</span>
    </slot>
    <slot name="placeholder" />
  </figure>
</template>

<script>
import Component from '../../base/Component'

const NjImage = Component.extend({
  components: {},
  inheritAttrs: false,
  props: {
    src: {
      type: [String, Object],
      default: null
    },
    disableLazyLoad: {
      type: Boolean,
      default: false
    },
    caption: {
      type: String,
      default: ''
    },
    copyright: {
      type: String,
      default: ''
    },
    usePlaceholder: {
      type: Boolean,
      default: true
    },
    placeholderImage: {
      type: String,
      default: ''
    },
    classes: {
      type: Object,
      default() {
        return {
          aspectRatio: '',
          image: 'object-cover'
        }
      }
    }
  },
  computed: {
    srcImg() {
      return typeof this.src === 'string'
        ? this.src
        : this.src?.opt
        ? this.src.opt
        : null
    },
    srcAlt() {
      if (this.src.alt) {
        return this.src.alt
      } else {
        return 'imagemage'
      }
    },
    srcset() {
      if (Object.prototype.hasOwnProperty.call(this.src, 'srcset')) {
        return this.src.srcset
      }
      return false
    },
    hasAspectRatio() {
      return (
        /aspect-ratio-/.test(this.getThemeClass('aspectRatio')) &&
        'has-aspect-ratio'
      )
    }
  }
})

export default NjImage
</script>
